    private void btt_IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btt_IngresarActionPerformed
        
        try {
            String contrasenia = (String.valueOf(jPassword.getPassword()));
            String usuario = txt_Usuario.getText();
            if( !usuario.equals("administrador") || !contrasenia.equals("202100229")){
                JOptionPane.showMessageDialog(null, "\nValores incorrectos", "Error", JOptionPane.ERROR_MESSAGE);             
            }else{
                VentanaMenu ventanaMenu = new VentanaMenu();
                ventanaMenu.setVisible(true);
                this.setVisible(false);
                new VentanaLogin().setVisible(false);
            }
        }catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "\nNo dejar campos vacios", "Error", JOptionPane.ERROR_MESSAGE);          
        } //java.lang.NumberFormatException
    }//GEN-LAST:event_btt_IngresarActionPerformed

